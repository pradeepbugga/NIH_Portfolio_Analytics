# This script contains functions to create and interact with the database tables for NIH grant data

from core.ingest.normalize import normalize_name

# Creates the Organizations table to store information about organizations.

def create_organizations_table(cur):
    cur.execute('''
        CREATE TABLE IF NOT EXISTS Organizations (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,  
        name TEXT,
        city TEXT,
        state TEXT,
        country TEXT,
        UNIQUE(name,city,state,country)            
        )''')

# Creates the ResearchGrants table to store information about research grants.

def create_research_grants_table(cur):
    cur.execute('''
        CREATE TABLE IF NOT EXISTS ResearchGrants (
        grant_id TEXT PRIMARY KEY, 
        fiscal_year INTEGER NOT NULL, 
        project_num TEXT NOT NULL,
        subproject_id TEXT,
        core_project_num TEXT NOT NULL,            
        contact_pi_id TEXT,
        organization_id INTEGER,
        project_start_date DATE,
        project_end_date DATE,
        budget_start_date DATE,
        budget_end_date DATE NOT NULL,
        project_title TEXT NOT NULL,
        abstract TEXT,
        phr TEXT,
        agency_ic TEXT,
        total_award_amount REAL DEFAULT 0.0,
        org_resolution_status TEXT,
        ingest_id TEXT,
        content_hash TEXT,
        record_version INTEGER DEFAULT 1,
        record_updated_at TIMESTAMP,
        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,             
        FOREIGN KEY(organization_id) REFERENCES Organizations (id)       
        )''')


# Creates the PIs table to store information about principal investigators.

def create_pis_table(cur):
    cur.execute('''
        CREATE TABLE IF NOT EXISTS PIs (
        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,  
        canonical_name TEXT NOT NULL UNIQUE,
        first_name TEXT,
        middle_name TEXT,
        last_name TEXT            
        )''')

# Creates the RGrantPIs table to associate research grants with principal investigators.

def create_rgrant_pis_table(cur):
    cur.execute('''
        CREATE TABLE IF NOT EXISTS RGrantPIs (
        grant_id TEXT,  
        pi_id INTEGER,
        is_contact_pi BOOLEAN,
        PRIMARY KEY (grant_id, pi_id),
        FOREIGN KEY(grant_id) REFERENCES ResearchGrants(grant_id),
        FOREIGN KEY(pi_id) REFERENCES PIs(id)   
        )''')
# Creates the IngestRuns table to track the status and metrics of each ingest run.

def create_ingest_runs_table(cur):
    
    cur.execute('''
        CREATE TABLE IF NOT EXISTS IngestRuns (
            ingest_id TEXT PRIMARY KEY,
            started_at TIMESTAMP NOT NULL,
            completed_at TIMESTAMP,
            num_inserted INTEGER DEFAULT 0,
            num_updated INTEGER DEFAULT 0,
            num_skipped INTEGER DEFAULT 0,
            num_errors INTEGER DEFAULT 0,
            status TEXT,
            notes TEXT
        ); ''')
# Creates the IngestErrors table to record errors encountered during the ingest process for specific grants.

def create_ingest_errors_table(cur):
    cur.execute('''
    CREATE TABLE IF NOT EXISTS IngestErrors (
    ingest_id TEXT,
    grant_id TEXT,
    error_type TEXT,
    error_message TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
    );''')

# Creates the GrantEmbeddings table to store embeddings for each grant.

def create_grant_embeddings_table(cur):
    cur.execute('''
    CREATE TABLE IF NOT EXISTS GrantEmbeddings (
    grant_id TEXT PRIMARY KEY,
    content_hash TEXT NOT NULL,
    embedding vector(768),
    embedding_model TEXT NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    is_valid BOOLEAN DEFAULT TRUE
    );''')


def insert_organization(cur, org: dict) -> int:
   
    # Inserts an organization into the database if it doesn't already exist.
    # Returns the organization ID.
    
    orgname = org.get("name") or ""
    orgcity = org.get("city") or "" 
    orgstate = org.get("state") or ""
    orgcountry = org.get("country") or ""


    cur.execute(''' INSERT INTO Organizations (name, city, state , country) 
                    VALUES(%s, %s, %s, %s)
                    ON CONFLICT(name, city, state, country) DO NOTHING
                    RETURNING id;
                    ''', (orgname, orgcity, orgstate, orgcountry))

    cur.execute(
        '''SELECT id FROM Organizations
           WHERE name = %s
             AND city = %s
             AND country = %s
             AND (state = %s
                  OR (state IS NULL AND %s = '')
                  OR (state IS NULL AND %s IS NULL))''',
        (orgname, orgcity, orgcountry, orgstate, orgstate, orgstate)
    )
    row = cur.fetchone()
    if not row:
        raise ValueError(f"Failed to insert or find organization")
    return row[0]

