def insert_pis(cur, grant_id: str, pis: list):
    
    # Inserts principal investigators into the database and links them to the research grant.
    
    for investigator in pis:
        
        # Normalize the investigator's name to extract first, middle, last, and canonical forms

        first, middle, last, canonical = normalize_name(investigator.get("full_name"))

        if not canonical:
            continue
        
        # Insert the PI into the database, or update if they already exist
        cur.execute("""
                INSERT INTO PIs (canonical_name, first_name, middle_name, last_name)
                VALUES (%s, %s, %s, %s) RETURNING id",
                ON CONFLICT (canonical_name) DO UPDATE 
                SET canonical_name = EXCLUDED.canonical_name -- This is a trick to force a RETURNING id
                RETURNING id
                """, (canonical, first, middle, last))

        row = cur.fetchone()
        if not row:
            cur.execute("SELECT id FROM PIs WHERE canonical_name = %s", (canonical,))
               
        else:
            pi_id = row[0] 

        # Link the PI to the research grant
        cur.execute("""
            INSERT INTO RGrantPIs (grant_id, pi_id, is_contact_pi) 
            VALUES (%s,%s,%s)
            ON CONFLICT(grant_id, pi_id) 
            DO UPDATE SET is_contact_pi = EXCLUDED.is_contact_pi
            """, (grant_id, pi_id, investigator.get("is_contact_pi")))

def insert_ingest_run(cur, ingest_id: str):
    
    # Inserts a new ingest run record into the database.
    
    cur.execute(
        """
        INSERT INTO IngestRuns (ingest_id, started_at, status)
        VALUES (%s, CURRENT_TIMESTAMP, 'running')
        """,
        (ingest_id,)
    )

def update_ingest_run_status(cur, ingest_id: str, metrics: dict):
    
    # Updates the status of an ingest run record in the database.
   

    cur.execute(
        """
        UPDATE IngestRuns
        SET completed_at = CURRENT_TIMESTAMP,
            status = 'completed',
            num_inserted = %s,
            num_updated = %s,
            num_skipped = %s,
            num_errors = %s
        WHERE ingest_id = %s
        """,
        (metrics["num_inserted"], metrics["num_updated"], metrics["num_skipped"], metrics["num_errors"], ingest_id)
    )
# Marks an ingest run as failed and records the error message.
def mark_ingest_run_failed(cur, ingest_id: str, error_message: str):
    cur.execute(
        """
        UPDATE IngestRuns
        SET
            status = 'failed',
            completed_at = CURRENT_TIMESTAMP,
            notes = %s
        WHERE ingest_id = %s
        """,
        (error_message[:1000], ingest_id)  # truncate defensively
    )

# Records an error encountered during the ingest process for a specific grant.
def record_error(cur, ingest_id, grant_id, error_type, message):
    cur.execute(
        """
        INSERT INTO IngestErrors
        (ingest_id, grant_id, error_type, error_message)
        VALUES (%s, %s, %s, %s)
        """,
        (ingest_id, grant_id, error_type, message)
    )
# Marks the embeddings for a specific grant as invalid.
def update_grant_embeddings(cur, grant_id: str):
    cur.execute('''
        UPDATE GrantEmbeddings
        SET is_valid = FALSE
        WHERE grant_id = %s''',
        (grant_id,))

